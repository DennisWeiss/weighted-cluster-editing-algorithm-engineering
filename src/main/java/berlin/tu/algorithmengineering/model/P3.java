package berlin.tu.algorithmengineering.model;

public class P3 {
    private Vertex u;
    private Vertex v;
    private Vertex w;

    public P3(Vertex u, Vertex v, Vertex w) {
        this.u = u;
        this.v = v;
        this.w = w;
    }

    public Vertex getU() {
        return u;
    }

    public void setU(Vertex u) {
        this.u = u;
    }

    public Vertex getV() {
        return v;
    }

    public void setV(Vertex v) {
        this.v = v;
    }

    public Vertex getW() {
        return w;
    }

    public void setW(Vertex w) {
        this.w = w;
    }
}
